void PrintImage(int[,] image)
{
    for(int i = 0; i < image.GetLength(0); i++)//счетчик/строки//конструкция замена числа строк на этот элемент
    {
        for(int j = 0; j < image.GetLength(1); j++)//счетчик/столбцы//замена
        {
            //Console.Write($"{image[i, j]} ");//пробежались по всем столбцам в строке
            if(image[i,j]  == 0 ) Console.Write($" ");// пробел если эта точка равна нулю
            else Console.Write($"+");//если не ноль, то печатаем например плюс
        }
    Console.WriteLine();//переход на новую строку
    }
}

void FillImage(int row, int col)// _______________          
{                                    //           |
                                     //           |    
    if(pic[row, col] == 0)           //           |
    {                                 //          |
        pic[row, col] = 1;            //          |
        FillImage( row - 1, col);// рекурсия - метод - вызывает сам себя!!!
        FillImage( row, col - 1);
        FillImage( row + 1, col);
        FillImage( row, col + 1);
    }
}



